

int suma(int x, int y)
{
    int suma=x+y;
    return suma;
}

int resta(int x, int y)
{
    int resta=x-y;
    return resta;
}

int multiplicacion(int x,int y)
{
    int multiplica;
    multiplica= x*y;
    return multiplica;
}

float division(int x,int y)
{
    float divide;
    divide = (float)x/(float)y;
    return divide;
}

int factorial(int x)
{
    int contador;
    int factorial;
    contador=x;
    factorial=1;
    while(contador >=1)
    {
        factorial=factorial*contador;
        contador--;
    }
    return      ;
}
